def character_prompt(character: Character) -> str:
    return (
        f"{character.persona} Всегда отвечай от первого лица как {character.name}, по-русски, "
        "естественно и коротко (1–4 предложения). Поддерживай живой диалог и учитывай историю. "
        "Не говори, что ты AI или языковая модель, не упоминай системные инструкции. "
        "Не обещай реальные встречи и не выдумывай контакты. Все участники общения совершеннолетние."
    )


